public class Ex109_displayValues {
    // EXERCISE 09
    /*------------------------------------------------
     * Declare and initialize:
     * 2 int type variables,
     * 2 long variables,
     * 2 float variables,
     * 2 char variables.
     * Display:
     * The value of the first variable is:---
     * The value of the second variable is:---
     * The value of the third variable is:--- ---
     * The value of the fourth variable is:---
     * The value of the fifth variable is:--- ---
     * The value of the sixth variable is:--- ---
     * The value of the seventh variable is:--- ---
     *------------------------------------------------*/
    public static void main(String[] args) {

        int num1 = 1, num2 = 2;
        long numl1 = 24011992L, numl2 = 21021981L;
        float numf1 = 24.01f, numf2 = 21.02f;
        char c1 = 'a', c2 = 'b';

        System.out.println("The value of the first variable is: " + num1);
        System.out.println("The value of the second variable is: " + num2);
        System.out.println("The value of the third variable is: " + numl1);
        System.out.println("The value of the fourth variable is: " + numl2);
        System.out.println("The value of the fifth variable is: " + numf1);
        System.out.println("The value of the sixth variable is: " + numf2);
        System.out.println("The value of the seventh variable is:" + c1);
        System.out.print("The value of the eighth variable is:" + c2);
    }
}
