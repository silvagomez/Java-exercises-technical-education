public class Ex105_dataTypes2 {
    // EXERCISE 05
    /*------------------------------------------------
     * Declare a variable of each data type,
     * assign values to these variables
     * and display its content separated by commas.
     *------------------------------------------------*/
    public static void main(String[] args) {

        byte n1 = 1;
        int n2 = 24011992;
        float n3 = -2.4f;
        double n4 = 1992.01d;
        short n5 = -24;
        long n6 = 9876543210L;
        char var1 = 'a';
        boolean var2 = false;
        String var3 = "silvagomez.com";

        System.out.print(n1 + "," + n2 + "," + n3 + n4 + "," + n5 + "," + n6 + "," + var1 + "," + var2 + "," + var3);
    }
}
