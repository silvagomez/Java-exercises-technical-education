public class Ex104_displayByCommas {
    // EXERCISE 04
    /*------------------------------------------------
     * Declare a variable of different data types,
     * assign values to these variables
     * and display its content separated by commas.
     *------------------------------------------------*/
    public static void main(String[] args) {

        byte b1 = 1, b2 = 2, b3 = 4;
        int year = 1992;
        boolean var_boolean = true;
        String web = "silvagomez.com";

        System.out.print("Since " + b1 + ", " + b2 + b3 + ", " + year + ", " + var_boolean + ", " + web);
    }
}
