public class Ex103_dataTypes {
    // EXERCISE 03
    /*------------------------------------------------
     * Declare a variable of each data type,
     * assign values to these variables
     * and display its content.
     *------------------------------------------------*/
    public static void main(String[] args) {

        byte var_byte;
        short var_short;
        int var_int;
        float var_float;
        boolean var_boolean;
        char var_char;
        String var_string;

        var_byte = 127;
        var_short = 32767;
        var_int = 2147483647;
        var_float = 1.24f;
        var_boolean = true;
        var_char = 'a';
        var_string = "silvagomez.com";
        System.out.println(var_byte);
        System.out.println(var_short);
        System.out.println(var_int);
        System.out.println(var_float);
        System.out.println(var_boolean);
        System.out.println(var_char);
        System.out.print(var_string);

    }
}
