public class Ex108_displayConstant {
    // EXERCISE 08
    /*------------------------------------------------
     * Display the contents of several constants.
     *------------------------------------------------*/
    public static void main(String[] args) {

        final float PI = 3.14f;
        final byte SECONDS = 60;
        final byte HOUR = 24;

        System.out.println(PI);
        System.out.println(SECONDS);
        System.out.print(HOUR);
    }
}
